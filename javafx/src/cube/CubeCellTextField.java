package cube;

import javafx.scene.control.TextField;

public class CubeCellTextField extends TextField{

	private int x;
	
	private int y;
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
